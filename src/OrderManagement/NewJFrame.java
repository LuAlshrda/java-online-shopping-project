private void loadCustomers() {

    customerComboBox.removeAllItems();
    customerComboBox.addItem("Select Customer");

    if (customers == null || customers.isEmpty()) {
        return;
    }

    for (Customer customer : customers) {

        if (customer != null) {

            customerComboBox.addItem(
                    customer.getCustomerId()
                    + " - "
                    + customer.getCustomerName()
            );
        }
    }

    customerComboBox.setSelectedIndex(0);
}
private void loadProducts() {

    productComboBox.removeAllItems();

    if (displayedProducts == null) {
        displayedProducts = new ArrayList<>();
    } else {
        displayedProducts.clear();
    }

    productComboBox.addItem("Select Product");

    if (products == null || products.isEmpty()) {
        return;
    }

    for (Product product : products) {

        if (product != null
                && product.getProductStatus()
                == ProductStatus.AVAILABLE
                && product.getQuantity() > 0) {

            displayedProducts.add(product);

            productComboBox.addItem(
                    product.getProductId()
                    + " - "
                    + product.getProductName()
            );
        }
    }

    productComboBox.setSelectedIndex(0);

    jLabelUnitPrice.setText("0.00");
    jLabelTotalAmount.setText("0.00");
}
