private String generateOrderId() {

    int highestNumber = 0;

    for (Order order : orderManager.getOrders()) {

        if (order == null
                || order.getOrderId() == null) {
            continue;
        }

        String orderId =
                order.getOrderId().trim();

        if (orderId.startsWith("ORD")) {

            try {

                int number =
                        Integer.parseInt(
                                orderId.substring(3)
                        );

                if (number > highestNumber) {
                    highestNumber = number;
                }

            } catch (NumberFormatException e) {
                // Ignore invalid order IDs
            }
        }
    }

    return String.format(
            "ORD%04d",
            highestNumber + 1
    );
}


private void calculateTotal() {

    int selectedIndex =
            productComboBox.getSelectedIndex();

    if (selectedIndex <= 0
            || displayedProducts == null) {

        jLabelTotalAmount.setText("0.00");
        return;
    }

    int productIndex = selectedIndex - 1;

    if (productIndex < 0
            || productIndex >= displayedProducts.size()) {

        jLabelTotalAmount.setText("0.00");
        return;
    }

    String quantityText =
            jTextFieldQuantity.getText().trim();

    if (quantityText.isEmpty()) {

        jLabelTotalAmount.setText("0.00");
        return;
    }

    try {

        int quantity =
                Integer.parseInt(quantityText);

        if (quantity <= 0) {

            jLabelTotalAmount.setText("0.00");
            return;
        }

        Product selectedProduct =
                displayedProducts.get(productIndex);

        double total =
                selectedProduct.getPrice() * quantity;

        jLabelTotalAmount.setText(
                String.format("%.2f", total)
        );

    } catch (NumberFormatException e) {

        jLabelTotalAmount.setText("0.00");
    }
}
private void clearOrderForm() {

    if (customerComboBox.getItemCount() > 0) {
        customerComboBox.setSelectedIndex(0);
    }

    if (productComboBox.getItemCount() > 0) {
        productComboBox.setSelectedIndex(0);
    }

    jTextFieldQuantity.setText("");
    jTextFieldAddress.setText("");

    jLabelUnitPrice.setText("0.00");
    jLabelTotalAmount.setText("0.00");

    customerComboBox.requestFocus();
}
