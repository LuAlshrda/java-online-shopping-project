    private void btnCreateOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateOrderActionPerformed
 
     // Validate customer selection
    if (customerComboBox.getSelectedIndex() <= 0) {

        JOptionPane.showMessageDialog(
                this,
                "Please select a customer.",
                "Missing Customer",
                JOptionPane.WARNING_MESSAGE
        );

        return;
    }

    // Validate product selection
    if (productComboBox.getSelectedIndex() <= 0) {

        JOptionPane.showMessageDialog(
                this,
                "Please select a product.",
                "Missing Product",
                JOptionPane.WARNING_MESSAGE
        );

        return;
    }

    // Validate quantity field
    String quantityText =
            jTextFieldQuantity.getText().trim();

    if (quantityText.isEmpty()) {

        JOptionPane.showMessageDialog(
                this,
                "Please enter the quantity.",
                "Missing Quantity",
                JOptionPane.WARNING_MESSAGE
        );

        return;
    }

    int quantity;

    try {

        quantity = Integer.parseInt(quantityText);

        if (quantity <= 0) {

            JOptionPane.showMessageDialog(
                    this,
                    "Quantity must be greater than zero.",
                    "Invalid Quantity",
                    JOptionPane.WARNING_MESSAGE
            );

            return;
        }

    } catch (NumberFormatException e) {

        JOptionPane.showMessageDialog(
                this,
                "Quantity must be a whole number.",
                "Invalid Quantity",
                JOptionPane.ERROR_MESSAGE
        );

        return;
    }

    // Validate address
    String address =
            jTextFieldAddress.getText().trim();

    if (address.isEmpty()) {

        JOptionPane.showMessageDialog(
                this,
                "Please enter the delivery address.",
                "Missing Address",
                JOptionPane.WARNING_MESSAGE
        );

        return;
    }

    // Get safe indexes
    int customerIndex =
            customerComboBox.getSelectedIndex() - 1;

    int productIndex =
            productComboBox.getSelectedIndex() - 1;

    // Validate customer index
    if (customers == null
            || customerIndex < 0
            || customerIndex >= customers.size()) {

        JOptionPane.showMessageDialog(
                this,
                "The selected customer is invalid.",
                "Invalid Customer",
                JOptionPane.ERROR_MESSAGE
        );

        return;
    }

    // Validate product index
    if (displayedProducts == null
            || productIndex < 0
            || productIndex >= displayedProducts.size()) {

        JOptionPane.showMessageDialog(
                this,
                "The selected product is invalid.",
                "Invalid Product",
                JOptionPane.ERROR_MESSAGE
        );

        return;
    }

    // Get selected objects
    Customer selectedCustomer =
            customers.get(customerIndex);

    Product selectedProduct =
            displayedProducts.get(productIndex);

    // Check stock before creating order
    if (quantity > selectedProduct.getQuantity()) {

        JOptionPane.showMessageDialog(
                this,
                "Only "
                + selectedProduct.getQuantity()
                + " item(s) are available.",
                "Insufficient Stock",
                JOptionPane.WARNING_MESSAGE
        );

        return;
    }

    // Generate order ID
    String orderId = generateOrderId();

    // Create order through OrderManager
    String result = orderManager.createOrder(
            orderId,
            selectedCustomer,
            selectedProduct,
            quantity,
            address
    );

    // Check result
    if (result.startsWith("Success")) {
double totalAmount =
        selectedProduct.getPrice() * quantity;

String orderSummary =
        "Order created successfully!"
        + "\n\nOrder ID: " + orderId
        + "\nCustomer: "
        + selectedCustomer.getCustomerName()
        + "\nProduct: "
        + selectedProduct.getProductName()
        + "\nQuantity: " + quantity
        + "\nUnit Price: "
        + String.format(
                "%.2f",
                selectedProduct.getPrice()
        )
        + "\nTotal Amount: "
        + String.format(
                "%.2f",
                totalAmount
        )
        + "\nDelivery Address: "
        + address;

JOptionPane.showMessageDialog(
        this,
        orderSummary,
        "Order Created",
        JOptionPane.INFORMATION_MESSAGE
        
        );

        // Reload product data because stock changed
        productManager = new ProductManager();
        products = productManager.getProducts();

        loadProducts();
        clearOrderForm();

    } else {

        JOptionPane.showMessageDialog(
                this,
                result,
                "Order Failed",
                JOptionPane.WARNING_MESSAGE
        );
    }
    }//GEN-LAST:event_btnCreateOrderActionPerformed

