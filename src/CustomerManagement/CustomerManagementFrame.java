    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String id = txtCustomerId.getText();
        String name = txtCustomerName.getText();
        String phone = txtPhone.getText();
        String email = txtEmail.getText();
        String address = txtAddress.getText();
        String city = txtCity.getText();
        String date = txtRegistrationDate.getText();

        String username;

    if (Session.isAdmin()) {

    username = JOptionPane.showInputDialog(
            this,
            "Enter the customer's login username:");

    if (username == null || username.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Username is required.");
        return;
    }

    } else {

        username = Session.getCurrentUser().getUsername();

        }
    CustomerStatus status = CustomerStatus.valueOf(cmbStatus.getSelectedItem().toString());
    Customer customer =
        new Customer(username,
             id,
             name,
             phone,
             email,
             address,
             city,
             date,
             status);
        
        if (!VAlidator.isEmpty(id)) {
        } else {
            JOptionPane.showMessageDialog(this,"Enter ID");
            return;
        }
        
       
        
        manager.addCustomer(customer);
        
        loadCustomerTable();

        JOptionPane.showMessageDialog(this,"Customer added successfully.");
        
        txtCustomerId.setText("");
        txtCustomerName.setText("");
        txtPhone.setText("");
        txtEmail.setText("");
        txtAddress.setText("");
        txtCity.setText("");
        txtRegistrationDate.setText("");
        cmbStatus.setSelectedIndex(0);
    }//GEN-LAST:event_jButton2ActionPerformed
